package com.mice.crm.service;

public class CRMStasticsVO extends CRMStastics {

	private String searchBeginDate; //시작일
	private String searchEndDate; //종료일
	private String searchManager; //담당자
	private String searchPlaceIdx; //장소
	public String getSearchBeginDate() {
		return searchBeginDate;
	}
	public void setSearchBeginDate(String searchBeginDate) {
		this.searchBeginDate = searchBeginDate;
	}
	public String getSearchEndDate() {
		return searchEndDate;
	}
	public void setSearchEndDate(String searchEndDate) {
		this.searchEndDate = searchEndDate;
	}
	public String getSearchManager() {
		return searchManager;
	}
	public void setSearchManager(String searchManager) {
		this.searchManager = searchManager;
	}
	public String getSearchPlaceIdx() {
		return searchPlaceIdx;
	}
	public void setSearchPlaceIdx(String searchPlaceIdx) {
		this.searchPlaceIdx = searchPlaceIdx;
	}
}
