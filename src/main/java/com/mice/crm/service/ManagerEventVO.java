package com.mice.crm.service;

public class ManagerEventVO extends ManagerEvent{

	private int sort_cnt;
	private String searchDepth;
	private String searchParent;
	private String searchSort;
	private String name_ko;
	private String manager_event;
	private String input_name;
	private String input_type;
	private String result;
	
	
	
	public int getSort_cnt() {
		return sort_cnt;
	}
	public void setSort_cnt(int sort_cnt) {
		this.sort_cnt = sort_cnt;
	}	
	
	public String getSearchDepth() {
		return searchDepth;
	}
	public void setSearchDepth(String searchDepth) {
		this.searchDepth = searchDepth;
	}
	
	public String getSearchParent() {
		return searchParent;
	}
	public void setSearchParent(String searchParent) {
		this.searchParent = searchParent;
	}	
	
	public String getSearchSort() {
		return searchSort;
	}
	public void setSearchSort(String searchSort) {
		this.searchSort = searchSort;
	}

	public String getName_ko() {
		return name_ko;
	}
	public void setName_ko(String name_ko) {
		this.name_ko = name_ko;
	}

	public String getManager_event() {
		return manager_event;
	}
	public void setManager_event(String manager_event) {
		this.manager_event = manager_event;
	}
	

	
	
	public String getInput_name() {
		return input_name;
	}
	public void setInput_name(String input_name) {
		this.input_name = input_name;
	}

	public String getInput_type() {
		return input_type;
	}
	public void setInput_type(String input_type) {
		this.input_type = input_type;
	}	
	
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}			
	
	
}
