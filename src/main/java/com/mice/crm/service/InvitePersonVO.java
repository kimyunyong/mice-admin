package com.mice.crm.service;

public class InvitePersonVO {
	private int idx;
	private int invite_idx;
	private int customer_idx;
	
	@Override
	public String toString() {
		return "InvitePersonVO [idx=" + idx + ", invite_idx=" + invite_idx + ", customer_idx=" + customer_idx + "]";
	}
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public int getInvite_idx() {
		return invite_idx;
	}
	public void setInvite_idx(int invite_idx) {
		this.invite_idx = invite_idx;
	}
	public int getCustomer_idx() {
		return customer_idx;
	}
	public void setCustomer_idx(int customer_idx) {
		this.customer_idx = customer_idx;
	}
}
