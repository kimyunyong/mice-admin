package com.mice.crm.service;

public class PromotionAttachFile {
	private int idx = -1;
	private int promotion_idx = -1;
	private String file_id = "";
	private String reg_date = "";
	private String reg_id = "";
	private String promotion_type = "";
	
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public int getPromotion_idx() {
		return promotion_idx;
	}
	public void setPromotion_idx(int promotion_idx) {
		this.promotion_idx = promotion_idx;
	}
	public String getFile_id() {
		return file_id;
	}
	public void setFile_id(String file_id) {
		this.file_id = file_id;
	}
	public String getReg_date() {
		return reg_date;
	}
	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	public String getReg_id() {
		return reg_id;
	}
	public void setReg_id(String reg_id) {
		this.reg_id = reg_id;
	}
	
	
	public String getPromotion_type() {
		return promotion_type;
	}
	public void setPromotion_type(String promotion_type) {
		this.promotion_type = promotion_type;
	}
		
	
}
