package com.mice.crm.service;

public class EventAttachFile {
	private int idx = -1;
	private int crm_event_idx = -1;
	private String type = "";
	private String file_id = "";
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public int getCrm_event_idx() {
		return crm_event_idx;
	}
	public void setCrm_event_idx(int crm_event_idx) {
		this.crm_event_idx = crm_event_idx;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getFile_id() {
		return file_id;
	}
	public void setFile_id(String file_id) {
		this.file_id = file_id;
	}
	
}
